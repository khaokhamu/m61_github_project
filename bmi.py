# รับค่าน้ำหนัก (กิโลกรัม) และส่วนสูง (เซนติเมตร)
weight = float(input("กรุณากรอกน้ำหนัก (กิโลกรัม): "))
height_cm = float(input("กรุณากรอกส่วนสูง (เซนติเมตร): "))

# แปลงส่วนสูงจากเซนติเมตรให้เป็นเมตร
height_m = height_cm / 100

# คำนวณค่า BMI (สูตร: น้ำหนัก ตัวหารด้วย ส่วนสูงยกกำลังสอง)
bmi = weight / (height_m ** 2)

# แสดงค่า BMI 
print(f"ค่า BMI ของคุณคือ: {bmi:.2f}")

# แปลผลสุขภาพตามเกณฑ์มาตรฐาน 
if bmi < 18.5:
    status = "น้ำหนักน้อย / ผอมเกินไป"
elif bmi < 23.0:
    status = "น้ำหนักปกติ / สุขภาพดี"
elif bmi < 25.0:
    status = "น้ำหนักเกิน / เริ่มตุ้ย"
elif bmi < 30.0:
    status = "อ้วน / ตุ้ยุนุ้ยระดับ 1"
else:
    status = "อ้วนมาก / ตุ้ยนุ้ยระดับ 2"

print(f"แปลผลสุขภาพ: {status}")